-----------------------------
--NGUOIBAN
CREATE TABLE VENDOR
(
 V_CODE VARCHAR(10) PRIMARY KEY NOT NULL,
 V_NAME NVARCHAR(255),
 V_ADDRESS NVARCHAR(255),
 COUNTRY NVARCHAR(255),
 V_PHONE VARCHAR(20),
 V_EMAIL NVARCHAR(255)
)
-----------------------------
--PHANLOAIHANG
CREATE TABLE CATEGORIES
(
CATEGORY_CODE VARCHAR(10) PRIMARY KEY NOT NULL,
CAT_DESCRIPT NVARCHAR(255)
)
-----------------------------
--SANPHAM
CREATE TABLE PRODUCT
(
 P_CODE VARCHAR(10) PRIMARY KEY NOT NULL,
 P_DESCRIPT NVARCHAR(255),
 CATEGORY_CODE VARCHAR(10) REFERENCES CATEGORIES,
 P_QOH INT,
 P_PRICE MONEY,
 P_RATING FLOAT,
 STT NVARCHAR(255) --STATUS
)

-----------------------------
--DONDATHANG
CREATE TABLE ORDERS
(
 ORDER_CODE VARCHAR(10) PRIMARY KEY NOT NULL,
 CUS_CODE VARCHAR(10) REFERENCES CUSTOMER(CUS_CODE),
 ORD_DATE SMALLDATETIME,
 DUE_DATE SMALLDATETIME,
 SHIP_COST MONEY,
 DELIVERY_TYPE NVARCHAR(255) NOT NULL DEFAULT N'VẬN CHUYỂN THƯỜNG', --PHƯƠNG THỨC VẬN CHUYỂN
 PAYMENT_TYPE NVARCHAR(255), --PTHUC THANH TOÁN 
 CUS_NOTE NVARCHAR(255),
 STT_ORDER NVARCHAR(255), --STATUS
 COIN_USED INT
)

-----------------------------
--CTĐH
CREATE TABLE ORDER_DETAILS
(
ORDER_DETAIL_NUM VARCHAR(10),
ORDER_CODE VARCHAR(10) REFERENCES ORDERS(ORDER_CODE),
PRIMARY KEY(ORDER_DETAIL_NUM, ORDER_CODE),
P_CODE VARCHAR(10) REFERENCES PRODUCT(P_CODE),
V_CODE VARCHAR(10) REFERENCES VENDOR (V_CODE),
UNITS INT,
PRICES INT
)
-----------------------------
--QUANGCAO
CREATE TABLE ADS
(
 ADS_CODE VARCHAR(10) PRIMARY KEY NOT NULL,
 P_CODE VARCHAR(10) REFERENCES PRODUCT,
 V_CODE VARCHAR(10) REFERENCES VENDOR (V_CODE),
 ADS_FULLNAME NVARCHAR(255),
 ADS_TYPE NVARCHAR(255)
)
-----------------------------
--YKIENPHANHOI
CREATE TABLE FEEDBACKS
(
 FB_ID INT NOT NULL,
 CUS_CODE VARCHAR(10) REFERENCES CUSTOMER(CUS_CODE),
 P_CODE VARCHAR(10) REFERENCES PRODUCT(P_CODE),
 RATING INT,
 COMMENT NVARCHAR(255)
)
-----------------------------
--NHANVIEN
CREATE TABLE EMPLOYEES
(
 EMP_CODE VARCHAR(10) PRIMARY KEY NOT NULL,
 EMP_NAME NVARCHAR(255),
 EMP_PHONE VARCHAR(20),
 EMP_DESCRIPT NVARCHAR(255)
)
-----------------------------
--HOTROKHACHHANG
CREATE TABLE CUS_SP
(
 SP_CODE VARCHAR(10) PRIMARY KEY NOT NULL,
 CUS_CODE VARCHAR(10) REFERENCES CUSTOMER(CUS_CODE),
 EMP_CODE VARCHAR(10) REFERENCES EMPLOYEES(EMP_CODE),
 SP_DESCRIPT NVARCHAR(255),
 SP_DATE SMALLDATETIME
)

-----------------------------
--VANCHUYENHANG
CREATE TABLE SHIP
(
 SHIP_CODE VARCHAR(10) PRIMARY KEY NOT NULL,
 DELIVERY_NAME NVARCHAR(255), --TÊN ĐƠN VỊ VẬN CHUYỂN 
 V_CODE VARCHAR(10) REFERENCES VENDOR (V_CODE),
 CUS_NOTE NVARCHAR(255),
 V_NOTE NVARCHAR(255)
)
-----------------------------
--XU 
CREATE TABLE COIN 
(
COIN_ID VARCHAR(10) PRIMARY KEY NOT NULL,
CUS_CODE VARCHAR(10) REFERENCES CUSTOMER(CUS_CODE),
UNITS_COIN INT
)
-----------------------------
--GIAMGIA
CREATE TABLE DISCOUNT
(
DISCOUNT_ID VARCHAR(10) PRIMARY KEY,
ORDER_CODE VARCHAR(10) REFERENCES ORDERS(ORDER_CODE),
V_CODE VARCHAR(10) REFERENCES VENDOR (V_CODE),
PERCENT_DC FLOAT
)

